from bfs import bfs


def test_bfs_path():
    graph = [
        [0, 1, 0],
        [0, 0, 1],
        [0, 0, 0],
    ]
    path, steps = bfs(graph, 0, 2)

    assert path == [0, 1, 2]
    assert steps >= 2


def test_bfs_no_path():
    graph = [
        [0, 1, 0],
        [0, 0, 0],
        [0, 0, 0],
    ]
    assert bfs(graph, 0, 2) is None

